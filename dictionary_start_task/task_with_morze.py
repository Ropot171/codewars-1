def test_morze_word():
    assert morze_word('Я') == '.-.-'
    assert morze_word('Москва') == '-- --- ... -.- .-- .-'
    assert morze_word('Я люблю Питон') == '.-.- -...- .-.. ..-- -... .-.. ..-- -...- .--. .. - --- -.'
    assert morze_word('Сергей Балакирев') == '... . .-. --. . .--- -...- -... .- .-.. .- -.- .. .-. . .--'

def test_morze_code():
    assert morze_code('.-- ... . -...- .-- . .-. -. ---') == 'все верно'



